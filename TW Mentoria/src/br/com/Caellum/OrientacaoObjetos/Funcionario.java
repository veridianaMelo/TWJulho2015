package br.com.Caellum.OrientacaoObjetos;

public class Funcionario {

	String nome;
	String departamento;
	double salario;
	Data dataDeEntrada;
	String rg;
	
	
	public void recebeAumento(double valor){
		this.salario = this.salario + valor;
		
	}
	
	double calculoGanhoAtual(){
		return this.salario * 12;
		
	}
	
	void mostra(){
		System.out.println("Nome Funcionário:" + this.nome);
		System.out.println("Departamento:" + this.departamento);
		System.out.println("Salário:" + this.salario);
		System.out.println("Dia Admissão" + this.dataDeEntrada.Dia);
		System.out.println("Mês Admissão" + this.dataDeEntrada.Mes);
		System.out.println("Ano Admissão" + this.dataDeEntrada.Ano);
		System.out.println("RG" + this.rg);
	}
	
}
